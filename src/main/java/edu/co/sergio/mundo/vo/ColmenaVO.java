package edu.co.sergio.mundo.vo;

public class ColmenaVO {
	private int id;
	private int poblacion;
        private String calidad;
        private boolean reina;
        private boolean miel;
        private int panalCera;
        private int panalAlimento;
        private int panalCria;
        private int panalVacio;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getPoblacion() {
        return poblacion;
    }

    public void setPoblacion(int poblacion) {
        this.poblacion = poblacion;
    }

    public String getCalidad() {
        return calidad;
    }

    public void setCalidad(String calidad) {
        this.calidad = calidad;
    }

    public boolean isReina() {
        return reina;
    }

    public void setReina(boolean reina) {
        this.reina = reina;
    }

    public boolean isMiel() {
        return miel;
    }

    public void setMiel(boolean miel) {
        this.miel = miel;
    }

    public int getPanalCera() {
        return panalCera;
    }

    public void setPanalCera(int panalCera) {
        this.panalCera = panalCera;
    }

    public int getPanalAlimento() {
        return panalAlimento;
    }

    public void setPanalAlimento(int panalAlimento) {
        this.panalAlimento = panalAlimento;
    }

    public int getPanalCria() {
        return panalCria;
    }

    public void setPanalCria(int panalCria) {
        this.panalCria = panalCria;
    }

    public int getPanalVacio() {
        return panalVacio;
    }

    public void setPanalVacio(int panalVacio) {
        this.panalVacio = panalVacio;
    }
	
}
